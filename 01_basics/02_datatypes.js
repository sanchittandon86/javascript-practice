console.log(typeof undefined); // undefined is a type in JS
console.log(typeof 0); // 0 is a number in JS
console.log(typeof 1.5); // 1.5 is a number in JS
console.log(typeof null); // null is an object in JS
console.log(typeof true); // true is a boolean in JS
console.log(typeof "Hello World"); // "Hello World" is a string in JS