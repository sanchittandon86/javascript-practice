// Data Types in JavaScript

// 1. Primitive Data Types -> 7
// - Number -> for numbers (integers, floats)
// - BigInt (ES11) -> for large integers
// - String -> for text (single/double quotes)
// - Boolean -> for true/false
// - Null -> for standalone value
// - Undefined -> for absence of value
// - Symbol (ES6) -> for unique identifiers

// 2. Reference Data Types
// - Object
// - Array
// - Function

"use strict"; // treat all JS code as newer version

